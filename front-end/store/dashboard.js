export const mutations = {
  addSubdomain(state, info) {
    state[info.domain].subdomains.push({
      exclude: false,
      name: info.subdomain,
      techs: [
        {
          name: 'VueJs',
          ver: '2.5.4',
          cves: ['This is a dummy CVE 1', 'Dummy CVE 2', 'CVE 3'],
          color: '#42b883',
        },
        {
          name: 'jQuery',
          ver: '1.9.4',
          cves: ['jQuery CVE this is'],
          color: '0769ad',
        },
      ],
    })
  },
  excludeToggle(state, info) {
    let subdomains = state[info.domain].subdomains
    for (const subdomain of subdomains) {
      if (subdomain.name == info.subdomain) {
        subdomain.exclude = !subdomain.exclude
      }
    }
  },
}

